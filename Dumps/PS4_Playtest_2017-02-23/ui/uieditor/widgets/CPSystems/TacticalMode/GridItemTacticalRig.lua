			condition = function ( menu, element, event )
				return not DoesCACItemHaveConsumablesRemaining( menu, element, controller )
			end
		},
		{
			stateName = "EquippedFullyUpgraded",
			condition = function ( menu, element, event )
				return IsCACItemEquipped( menu, element, controller ) and IsCACItemUpgraded( menu, element, controller )
			end
		}
	} )
	self:linkToElementModel( self, "itemIndex", true, function ( model )
		menu:updateElementState( self, {
			name = "model_validation",
			menu = menu,
			modelValue = Engine.GetModelValue( model ),
			modelName = "itemIndex"
		} )
	end )
	LUI.OverrideFunction_CallOriginalSecond( self, "close", function ( self )
		self.BoxButtonLrgInactiveDiag:close()
		self.BoxButtonLrgInactive:close()
		self.BoxButtonLrgIdle:close()
		self.tokenUnlock:close()
		self.LabelButton:close()
		self.lockedIcon:close()
		self.IconNew:close()
		self.FocusBarT:close()
		self.FocusBarB:close()
		self.FocusBorder:close()
		self.ConsumableLabel:close()
		self.HeaderLabel:close()
		self.RestrictedItemWarning:close()
		self.GridItemUpgradeIconTacticalRig:close()
		self.tokenUnlockable:close()
		self.itemImageSolid:close()
		self.itemImage:close()
	end )
	if PostLoadFunc then
		PostLoadFunc( self, controller, menu )
	end
	return self
