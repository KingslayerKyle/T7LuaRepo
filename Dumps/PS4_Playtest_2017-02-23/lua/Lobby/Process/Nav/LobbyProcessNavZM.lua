require( "lua.Lobby.Process.LobbyProcess" )

Lobby.Core.AddProcessBack( LuaEnum.UI.LAN_ZM, LuaEnum.UI.MODE_SELECT_LAN, function ( controller, fromTarget, toTarget )
	return Lobby.ProcessNavigate.LeaveGameLobby( controller, fromTarget, toTarget, LuaEnum.LEAVE_WITH_PARTY.WITHOUT, "" )
end )
Lobby.Core.AddProcessForward( LuaEnum.UI.ONLINE_ZM, LuaEnum.UI.ONLINE_ZM_CUSTOM, function ( controller, fromTarget, toTarget )
	return Lobby.ProcessNavigate.ChangeToCustomGameLobby( controller, fromTarget, toTarget )
end )
Lobby.Core.AddProcessForward( LuaEnum.UI.ONLINE_ZM, LuaEnum.UI.ONLINE_ZM_PUBLIC, function ( controller, fromTarget, toTarget )
	return Lobby.ProcessNavigate.ChangeToPublicGameLobby( controller, fromTarget, toTarget )
end )
Lobby.Core.AddProcessBack( LuaEnum.UI.ONLINE_ZM, LuaEnum.UI.MODE_SELECT_ONLINE, function ( controller, fromTarget, toTarget, withParty )
	return Lobby.ProcessNavigate.LeaveGameLobby( controller, fromTarget, toTarget, withParty, "" )
end )
Lobby.Core.AddProcessBack( LuaEnum.UI.ONLINE_ZM_PUBLIC, LuaEnum.UI.ONLINE_ZM, function ( controller, fromTarget, toTarget, withParty )
	return Lobby.ProcessNavigate.ChangeToPrivateGameLobby( controller, fromTarget, toTarget, withParty )
end )
Lobby.Core.AddProcessBack( LuaEnum.UI.ONLINE_ZM_CUSTOM, LuaEnum.UI.ONLINE_ZM, function ( controller, fromTarget, toTarget, withParty )
	return Lobby.ProcessNavigate.ChangeToPrivateGameLobby( controller, fromTarget, toTarget, withParty )
end )
Lobby.Core.AddProcessForward( LuaEnum.UI.ONLINE_ZM, LuaEnum.UI.ONLINE_ZM_THEATER, function ( controller, fromTarget, toTarget )
	Lobby.Theater.OnSessionStart( toTarget )
	return Lobby.ProcessNavigate.ChangeToCustomGameLobby( controller, fromTarget, toTarget )
end )
Lobby.Core.AddProcessBack( LuaEnum.UI.ONLINE_ZM_THEATER, LuaEnum.UI.ONLINE_ZM, function ( controller, fromTarget, toTarget, withParty )
	Lobby.Theater.OnSessionEnd( fromTarget )
	Lobby.ProcessNavigate.SetupLobbyMapAndGameType( controller, toTarget )
	return Lobby.ProcessNavigate.ChangeToPrivateGameLobby( controller, fromTarget, toTarget, withParty )
end )
