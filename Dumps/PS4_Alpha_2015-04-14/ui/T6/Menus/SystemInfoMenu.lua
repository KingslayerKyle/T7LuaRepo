CoD.SystemInfo = {}
LUI.createMenu.SystemInfo = function ( controller )
	local systemInfoMenu = CoD.Menu.New( "SystemInfo" )
	systemInfoMenu:setOwner( controller )
	systemInfoMenu:addTitle( Engine.Localize( "MENU_SYSTEM_INFO_CAPS" ) )
	systemInfoMenu:registerEventHandler( "signed_out", CoD.Options.SignedOut_MPZM )
	systemInfoMenu:addLargePopupBackground()
	systemInfoMenu:addBackButton()
	local top = CoD.Menu.TitleHeight + 30
	local spacer = 5
	local versionNumberLabel = LUI.UIText.new()
	versionNumberLabel:setLeftRight( true, true, 0, 0 )
	versionNumberLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	versionNumberLabel:setFont( CoD.fonts.Default )
	versionNumberLabel:setAlignment( LUI.Alignment.Left )
	versionNumberLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_VERSION_NUMBER ) )
	systemInfoMenu:addElement( versionNumberLabel )
	top = top + CoD.textSize.Default + spacer
	local connectivityLabel = LUI.UIText.new()
	connectivityLabel:setLeftRight( true, true, 0, 0 )
	connectivityLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	connectivityLabel:setFont( CoD.fonts.Default )
	connectivityLabel:setAlignment( LUI.Alignment.Left )
	connectivityLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_CONNECTIVITY_INFO ) )
	systemInfoMenu:addElement( connectivityLabel )
	top = top + CoD.textSize.Default + spacer
	local natTypeLabel = LUI.UIText.new()
	natTypeLabel:setLeftRight( true, true, 0, 0 )
	natTypeLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	natTypeLabel:setFont( CoD.fonts.Default )
	natTypeLabel:setAlignment( LUI.Alignment.Left )
	natTypeLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_NAT_TYPE ) )
	systemInfoMenu:addElement( natTypeLabel )
	top = top + CoD.textSize.Default + spacer
	local bandwidthLabel = LUI.UIText.new()
	bandwidthLabel:setLeftRight( true, true, 0, 0 )
	bandwidthLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	bandwidthLabel:setFont( CoD.fonts.Default )
	bandwidthLabel:setAlignment( LUI.Alignment.Left )
	bandwidthLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_BANDWIDTH ) )
	systemInfoMenu:addElement( bandwidthLabel )
	top = top + CoD.textSize.Default + spacer
	local ipAddressLabel = LUI.UIText.new()
	ipAddressLabel:setLeftRight( true, true, 0, 0 )
	ipAddressLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	ipAddressLabel:setFont( CoD.fonts.Default )
	ipAddressLabel:setAlignment( LUI.Alignment.Left )
	ipAddressLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_IP_ADDRESS ) )
	systemInfoMenu:addElement( ipAddressLabel )
	top = top + CoD.textSize.Default + spacer
	local regionLabel = LUI.UIText.new()
	regionLabel:setLeftRight( true, true, 0, 0 )
	regionLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	regionLabel:setFont( CoD.fonts.Default )
	regionLabel:setAlignment( LUI.Alignment.Left )
	regionLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_GEOGRAPHICAL_REGION ) )
	systemInfoMenu:addElement( regionLabel )
	top = top + CoD.textSize.Default + spacer
	local connectionTypeLabel = LUI.UIText.new()
	connectionTypeLabel:setLeftRight( true, true, 0, 0 )
	connectionTypeLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	connectionTypeLabel:setFont( CoD.fonts.Default )
	connectionTypeLabel:setAlignment( LUI.Alignment.Left )
	connectionTypeLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_CONNECTION_TYPE ) )
	systemInfoMenu:addElement( connectionTypeLabel )
	top = top + CoD.textSize.Default + spacer
	local supportLabel = LUI.UIText.new()
	supportLabel:setLeftRight( true, true, 0, 0 )
	supportLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	supportLabel:setFont( CoD.fonts.Default )
	supportLabel:setAlignment( LUI.Alignment.Left )
	supportLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_CUSTOMER_SUPPORT_LINK ) )
	systemInfoMenu:addElement( supportLabel )
	if Engine.IsSuperUser( controller ) then
		top = top + CoD.textSize.Default + spacer
		local qLabel = LUI.UIText.new()
		qLabel:setLeftRight( true, true, 0, 0 )
		qLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
		qLabel:setFont( CoD.fonts.Default )
		qLabel:setAlignment( LUI.Alignment.Left )
		qLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_Q ) )
		systemInfoMenu:addElement( qLabel )
	end
	top = top + CoD.textSize.Default + spacer + CoD.textSize.Default + spacer + CoD.textSize.Default + spacer + CoD.textSize.Default + spacer
	local supportLabel = LUI.UIText.new()
	supportLabel:setLeftRight( true, true, 0, 0 )
	supportLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	supportLabel:setRGB( CoD.yellow.r, CoD.yellow.g, CoD.yellow.b )
	supportLabel:setFont( CoD.fonts.Default )
	supportLabel:setAlignment( LUI.Alignment.Left )
	supportLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_CONSOLE_ID ) )
	systemInfoMenu:addElement( supportLabel )
	top = top + CoD.textSize.Default + spacer
	local supportLabel = LUI.UIText.new()
	supportLabel:setLeftRight( true, true, 0, 0 )
	supportLabel:setTopBottom( true, false, top, top + CoD.textSize.Default )
	supportLabel:setRGB( CoD.yellow.r, CoD.yellow.g, CoD.yellow.b )
	supportLabel:setFont( CoD.fonts.Default )
	supportLabel:setAlignment( LUI.Alignment.Left )
	supportLabel:setText( Engine.GetSystemInfo( controller, CoD.SYSINFO_MAC_ADDRESS ) )
