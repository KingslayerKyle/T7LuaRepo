require( "lua.Lobby.Process.LobbyProcess" )

Lobby.Core.AddProcessBack( LobbyData.UITargets.UI_CPLOBBYLANGAME, LobbyData.UITargets.UI_MODESELECT, function ( controller, fromTarget, toTarget )
	return Lobby.ProcessNavigate.LeaveGameLobby( controller, fromTarget, toTarget, LobbyData.LEAVE_WITH_PARTY_WITHOUT, "" )
end )
Lobby.Core.AddProcessForward( LobbyData.UITargets.UI_CPLOBBYONLINE, LobbyData.UITargets.UI_CPLOBBYONLINECUSTOMGAME, function ( controller, fromTarget, toTarget )
	return Lobby.ProcessNavigate.CreateGameLobby( controller, fromTarget, toTarget )
end )
Lobby.Core.AddProcessForward( LobbyData.UITargets.UI_CPLOBBYONLINE, LobbyData.UITargets.UI_CPLOBBYONLINEPUBLICGAME, function ( controller, fromTarget, toTarget )
	return Lobby.ProcessNavigate.ChangeToPublicGameLobby( controller, fromTarget, toTarget )
end )
Lobby.Core.AddProcessBack( LobbyData.UITargets.UI_CPLOBBYONLINE, LobbyData.UITargets.UI_MODESELECT, function ( controller, fromTarget, toTarget, withParty )
	return Lobby.ProcessNavigate.LeaveGameLobby( controller, fromTarget, toTarget, withParty )
end )
Lobby.Core.AddProcessBack( LobbyData.UITargets.UI_CPLOBBYONLINECUSTOMGAME, LobbyData.UITargets.UI_CPLOBBYONLINE, function ( controller, fromTarget, toTarget )
	return Lobby.ProcessNavigate.LeaveGameLobby( controller, fromTarget, toTarget, LobbyData.LEAVE_WITH_PARTY_WITHOUT )
end )
Lobby.Core.AddProcessForward( LobbyData.UITargets.UI_CPLOBBYONLINEPUBLIC, LobbyData.UITargets.UI_CPLOBBYONLINEPUBLICGAME, function ( controller, fromTarget, toTarget )
	return Lobby.ProcessNavigate.CreatePublicGameLobby( controller, fromTarget, toTarget )
end )
Lobby.Core.AddProcessBack( LobbyData.UITargets.UI_CPLOBBYONLINEPUBLIC, LobbyData.UITargets.UI_CPLOBBYONLINE, function ( controller, fromTarget, toTarget )
	if Engine.IsLobbyHost( Enum.LobbyType.LOBBY_TYPE_PRIVATE ) then
		return Lobby.Process.Navigate( controller, fromTarget, toTarget, nil )
	else
		return Lobby.Process.ReloadPrivateLobby( controller, Engine.GetLobbyNetworkMode() )
	end
end )
Lobby.Core.AddProcessBack( LobbyData.UITargets.UI_CPLOBBYONLINEPUBLICGAME, LobbyData.UITargets.UI_CPLOBBYONLINE, function ( controller, fromTarget, toTarget )
	return Lobby.ProcessNavigate.LeaveGameLobby( controller, fromTarget, toTarget, LobbyData.LEAVE_WITH_PARTY_WITHOUT )
end )
