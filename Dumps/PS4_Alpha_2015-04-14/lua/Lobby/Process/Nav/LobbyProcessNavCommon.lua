Lobby.Core.AddProcessBack( LobbyData.UITargets.UI_MODESELECT, LobbyData.UITargets.UI_MAIN, function ( controller, fromTarget, toTarget )
	if Engine.IsLobbyHost( Enum.LobbyType.LOBBY_TYPE_PRIVATE ) then
		return Lobby.ProcessNavigate.LeavePrivateLobby( controller, fromTarget, toTarget )
	else
		return Lobby.Process.ReloadPrivateLobby( controller, Engine.GetLobbyNetworkMode() )
	end
end )
