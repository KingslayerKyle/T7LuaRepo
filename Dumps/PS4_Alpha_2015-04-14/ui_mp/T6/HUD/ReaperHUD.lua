CoD.ReaperHUD = {}
CoD.ReaperHUD.new = function ( controller )
	local self = CoD.AirVehicleHUD.new( "reaper", controller, 0, 1, 0, "digital_noise_filter_remote_mortar" )
	self:addIRToggle()
	self:addCrosshairDistance( "LAS" )
	self:addFakeHighAuto()
	self:addRecBox()
	self:addSignalBox()
	CoD.ReaperHUD.AddReticle( self )
	return self
end

CoD.ReaperHUD.AddReticle = function ( self )
	local reticle = LUI.UIImage.new()
	reticle:setLeftRight( false, false, -128, 128 )
	reticle:setTopBottom( false, false, -128, 128 )
	reticle:setRGB( self.colorR, self.colorG, self.colorB )
	reticle:setImage( RegisterMaterial( "mp_hud_reaper_reticle" ) )
	self:addElement( reticle )
	local compass = LUI.UIElement.new()
	compass:setLeftRight( false, false, -234, 234 )
	compass:setTopBottom( true, false, 50, 114 )
	compass:setImage( RegisterMaterial( "hud_horizontal_compass_white" ) )
	compass:setRGB( self.colorR, self.colorG, self.colorB )
	compass:setShaderVector( 0, 1, -0.5, 0, 0 )
	compass:setupHorizontalCompass()
	self:addElement( compass )
	if 0 == CoD.IS_KILLCAM_OR_SPECTATE( controller ) and not Engine.IsDemoPlaying() then
		local leftSideScaleContainer = CoD.SplitscreenScaler.new( nil, CoD.SplitscreenMultiplier )
		leftSideScaleContainer:setLeftRight( true, false, 0, 0 )
		leftSideScaleContainer:setTopBottom( false, true, 0, 0 )
		self.safeAreaContainer:addElement( leftSideScaleContainer )
		local exitPrompt = LUI.UIText.new()
		exitPrompt:setLeftRight( true, false, 2, 3 )
		exitPrompt:setTopBottom( false, true, -10 - CoD.textSize.Default, -10 )
		exitPrompt:setText( Engine.Localize( "MP_REMOTE_EXIT" ) )
		exitPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		exitPrompt:setFont( CoD.fonts.Default )
		leftSideScaleContainer:addElement( exitPrompt )
		local zoomPrompt = LUI.UIText.new()
		zoomPrompt:setLeftRight( true, false, 0, 1 )
		zoomPrompt:setTopBottom( false, true, -40 - CoD.textSize.Default, -40 )
		zoomPrompt:setText( Engine.Localize( "KILLSTREAK_INCREASE_ZOOM" ) )
		zoomPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		zoomPrompt:setFont( CoD.fonts.Default )
		leftSideScaleContainer:addElement( zoomPrompt )
		local movePrompt = LUI.UIText.new()
		movePrompt:setLeftRight( true, false, 0, 1 )
		movePrompt:setTopBottom( false, true, -70 - CoD.textSize.Default, -70 )
		movePrompt:setText( Engine.Localize( "MP_REAPER_FIRE_MISSILES" ) )
		movePrompt:setRGB( self.colorR, self.colorG, self.colorB )
		movePrompt:setFont( CoD.fonts.Default )
		leftSideScaleContainer:addElement( movePrompt )
	end
end

