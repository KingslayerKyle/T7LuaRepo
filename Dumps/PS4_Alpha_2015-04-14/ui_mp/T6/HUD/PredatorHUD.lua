CoD.PredatorHUD = {}
CoD.PredatorHUD.new = function ( controller )
	local self = CoD.AirVehicleHUD.new( "predator", controller )
	self:addCrosshairDistance( "DIST" )
	self:addFakeHighAuto()
	CoD.PredatorHUD.AddReticle( self, controller )
	return self
end

CoD.PredatorHUD.AddReticle = function ( self, controller )
	self:addCompass()
	local inner = LUI.UIImage.new()
	inner:setLeftRight( false, false, -256, 256 )
	inner:setTopBottom( false, false, -256, 256 )
	inner:setImage( RegisterMaterial( "mp_hud_sentry_inner" ) )
	self:addElement( inner )
	if CoD.IS_KILLCAM_OR_SPECTATE( controller ) == 0 and not Engine.IsDemoPlaying() then
		local boostHint = LUI.UIText.new()
		boostHint:setLeftRight( true, false, 0, 1 )
		boostHint:setTopBottom( false, true, -CoD.textSize.Default, 0 )
		boostHint:setText( Engine.Localize( "PLATFORM_REMOTE_MISSILE_BOOST" ) )
		boostHint:setFont( CoD.fonts.Default )
		boostHint:setRGB( self.colorR, self.colorG, self.colorB )
		self.leftButtonPrompts:addElement( boostHint )
		local brakeHint = LUI.UIText.new()
		brakeHint:setLeftRight( true, false, 0, 0 )
		brakeHint:setTopBottom( false, true, -30 - CoD.textSize.Default, -30 )
		brakeHint:setRGB( self.colorR, self.colorG, self.colorB )
		brakeHint:setText( Engine.Localize( "KILLSTREAK_REMOTE_MISSILE_BRAKE" ) )
		brakeHint:setFont( CoD.fonts.Default )
		self.leftButtonPrompts:addElement( brakeHint )
		brakeHint:registerEventHandler( "remote_missile_brake_disabled", function ()
			brakeHint:setAlpha( 0 )
		end )
		local steerHint = LUI.UIText.new()
		steerHint:setLeftRight( false, true, -1000, 0 )
		steerHint:setTopBottom( false, true, -30 - CoD.textSize.Default, -30 )
		steerHint:setText( Engine.Localize( "PLATFORM_REMOTE_MISSILE_STEER" ) )
		steerHint:setFont( CoD.fonts.Default )
		steerHint:setAlignment( LUI.Alignment.Right )
		steerHint:setRGB( self.colorR, self.colorG, self.colorB )
		self.rightButtonPrompts:addElement( steerHint )
		local deployHint = LUI.UIText.new()
		deployHint:setLeftRight( false, true, -1000, 0 )
		deployHint:setTopBottom( false, true, -CoD.textSize.Default, 0 )
		deployHint:setText( Engine.Localize( "KILLSTREAK_REMOTE_MISSILE_DEPLOY" ) )
		deployHint:setFont( CoD.fonts.Default )
		deployHint:setAlignment( LUI.Alignment.Right )
		deployHint:setRGB( self.colorR, self.colorG, self.colorB )
		self.rightButtonPrompts:addElement( deployHint )
	end
	local deployStatus = LUI.UIImage.new()
	deployStatus:setLeftRight( false, false, -128, 128 )
	deployStatus:setTopBottom( false, false, 141, 269 )
	deployStatus:setImage( RegisterMaterial( "mp_hud_cluster_status" ) )
	self:addElement( deployStatus )
end

