CoD.ChopperGunnerHUD = {}
CoD.ChopperGunnerHUD.new = function ( controller )
	local self = CoD.AirVehicleHUD.new( "chopperGunner", controller, 1, 0.5, 0, "digital_noise_filter_chopper_gunner" )
	self:addIRToggle()
	self:addCompass()
	self:addRecBox()
	self:addSignalBox()
	CoD.ChopperGunnerHUD.AddReticle( self )
	return self
end

CoD.ChopperGunnerHUD.AddReticle = function ( self )
	local rollContainer = LUI.UIElement.new()
	rollContainer:setAlpha( 0.75 )
	rollContainer:setupRollLevel( 1 )
	self:addElement( rollContainer )
	local inner = LUI.UIImage.new()
	inner:setLeftRight( false, false, -256, 256 )
	inner:setTopBottom( false, false, -128, 128 )
	inner:setImage( RegisterMaterial( "mp_hud_wasp_inner_white" ) )
	inner:setRGB( self.colorR, self.colorG, self.colorB )
	rollContainer:addElement( inner )
	local outerMaterial = RegisterMaterial( "mp_hud_sentry_outer_white" )
	local outerWidth = 512
	local outerHeight = 256
	local outerRightOffset = 80
	local outerBottomOffset = 20
	local adsContainer = LUI.UIElement.new()
	adsContainer:setupADSContainer()
	adsContainer:setShaderVector( 0, 0, 0.05, 0.05, 0 )
	adsContainer:setLeftRight( false, false, -outerRightOffset - outerWidth, outerRightOffset + outerWidth )
	adsContainer:setTopBottom( false, false, -outerBottomOffset - outerHeight, outerBottomOffset + outerHeight )
	self:addElement( adsContainer )
	local outerBottomRight = LUI.UIImage.new()
	outerBottomRight:setLeftRight( false, true, -outerWidth, 0 )
	outerBottomRight:setTopBottom( false, true, -outerHeight, 0 )
	outerBottomRight:setImage( outerMaterial )
	outerBottomRight:setRGB( self.colorR, self.colorG, self.colorB )
	adsContainer:addElement( outerBottomRight )
	local outerBottomLeft = LUI.UIImage.new()
	outerBottomLeft:setLeftRight( true, false, outerWidth, 0 )
	outerBottomLeft:setTopBottom( false, true, -outerHeight, 0 )
	outerBottomLeft:setImage( outerMaterial )
	outerBottomLeft:setRGB( self.colorR, self.colorG, self.colorB )
	adsContainer:addElement( outerBottomLeft )
	local outerTopRight = LUI.UIImage.new()
	outerTopRight:setLeftRight( false, true, -outerWidth, 0 )
	outerTopRight:setTopBottom( true, false, outerHeight, 0 )
	outerTopRight:setImage( outerMaterial )
	outerTopRight:setRGB( self.colorR, self.colorG, self.colorB )
	adsContainer:addElement( outerTopRight )
	local outerTopLeft = LUI.UIImage.new()
	outerTopLeft:setLeftRight( true, false, outerWidth, 0 )
	outerTopLeft:setTopBottom( true, false, outerHeight, 0 )
	outerTopLeft:setImage( outerMaterial )
	outerTopLeft:setRGB( self.colorR, self.colorG, self.colorB )
	adsContainer:addElement( outerTopLeft )
	local reticleSeparation = 28
	local adsReticleContainer = LUI.UIElement.new()
	adsReticleContainer:setupADSContainer()
	adsReticleContainer:setShaderVector( 0, 0, 0.2, 0.2, 0 )
	adsReticleContainer:setLeftRight( false, false, -reticleSeparation, reticleSeparation )
	adsReticleContainer:setTopBottom( false, false, -reticleSeparation, reticleSeparation )
	self:addElement( adsReticleContainer )
	local reticleSideSize = 16
	local reticleSideMaterial = RegisterMaterial( "mp_hud_reticle_side_round" )
	local reticleTop = LUI.UIImage.new()
	reticleTop:setLeftRight( false, false, -reticleSideSize / 2, reticleSideSize / 2 )
	reticleTop:setTopBottom( true, false, 0, reticleSideSize )
	reticleTop:setRGB( self.colorR, self.colorG, self.colorB )
	reticleTop:setImage( reticleSideMaterial )
	adsReticleContainer:addElement( reticleTop )
	local reticleBottom = LUI.UIImage.new()
	reticleBottom:setLeftRight( false, false, -reticleSideSize / 2, reticleSideSize / 2 )
	reticleBottom:setTopBottom( false, true, -reticleSideSize, 0 )
	reticleBottom:setRGB( self.colorR, self.colorG, self.colorB )
	reticleBottom:setZRot( 180 )
	reticleBottom:setImage( reticleSideMaterial )
	adsReticleContainer:addElement( reticleBottom )
	local reticleLeft = LUI.UIImage.new()
	reticleLeft:setLeftRight( true, false, 0, reticleSideSize )
	reticleLeft:setTopBottom( false, false, -reticleSideSize / 2, reticleSideSize / 2 )
	reticleLeft:setRGB( self.colorR, self.colorG, self.colorB )
	reticleLeft:setZRot( 90 )
	reticleLeft:setImage( reticleSideMaterial )
	adsReticleContainer:addElement( reticleLeft )
	local reticleRight = LUI.UIImage.new()
	reticleRight:setLeftRight( false, true, -reticleSideSize, 0 )
	reticleRight:setTopBottom( false, false, -reticleSideSize / 2, reticleSideSize / 2 )
	reticleRight:setRGB( self.colorR, self.colorG, self.colorB )
	reticleRight:setZRot( -90 )
	reticleRight:setImage( reticleSideMaterial )
	adsReticleContainer:addElement( reticleRight )
	local reticleDot = LUI.UIImage.new()
	reticleDot:setLeftRight( false, false, -4, 4 )
	reticleDot:setTopBottom( false, false, -4, 4 )
	reticleDot:setRGB( self.colorR, self.colorG, self.colorB )
	reticleDot:setImage( RegisterMaterial( "mp_hud_dot" ) )
	adsReticleContainer:addElement( reticleDot )
	if 0 == CoD.IS_KILLCAM_OR_SPECTATE( controller ) and not Engine.IsDemoPlaying() then
		local exitPrompt = LUI.UIText.new()
		exitPrompt:setLeftRight( true, false, 0, 0 )
		exitPrompt:setTopBottom( false, true, -60 - CoD.textSize.Default, -60 )
		exitPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		exitPrompt:setText( Engine.Localize( "MP_REMOTE_EXIT" ) )
		exitPrompt:setFont( CoD.fonts.Default )
		self.leftButtonPrompts:addElement( exitPrompt )
		local zoomPrompt = LUI.UIText.new()
		zoomPrompt:setLeftRight( true, false, 0, 0 )
		zoomPrompt:setTopBottom( false, true, -CoD.textSize.Default, 0 )
		zoomPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		zoomPrompt:setText( Engine.Localize( "KILLSTREAK_INCREASE_ZOOM" ) )
		zoomPrompt:setFont( CoD.fonts.Default )
		self.leftButtonPrompts:addElement( zoomPrompt )
		local movePrompt = LUI.UIText.new()
		movePrompt:setLeftRight( true, false, 0, 0 )
		movePrompt:setTopBottom( false, true, -30 - CoD.textSize.Default, -30 )
		movePrompt:setRGB( self.colorR, self.colorG, self.colorB )
		movePrompt:setText( Engine.Localize( "MP_HELI_NEW_LOCATION" ) )
		movePrompt:setFont( CoD.fonts.Default )
		self.leftButtonPrompts:addElement( movePrompt )
		local gunPrompt = LUI.UIText.new()
		gunPrompt:setLeftRight( false, true, -1000, 0 )
		gunPrompt:setTopBottom( false, true, -CoD.textSize.Default, 0 )
		gunPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		gunPrompt:setText( Engine.Localize( "MP_HELI_FIRE_MINIGUN" ) )
		gunPrompt:setFont( CoD.fonts.Default )
		gunPrompt:setAlignment( LUI.Alignment.Right )
		self.rightButtonPrompts:addElement( gunPrompt )
		local missilePrompt = LUI.UIText.new()
		missilePrompt:setLeftRight( false, true, -1000, 0 )
		missilePrompt:setTopBottom( false, true, -30 - CoD.textSize.Default, -30 )
		missilePrompt:setRGB( self.colorR, self.colorG, self.colorB )
		missilePrompt:setText( Engine.Localize( "MP_HELI_FIRE_MISSILES" ) )
		missilePrompt:setFont( CoD.fonts.Default )
		missilePrompt:setAlignment( LUI.Alignment.Right )
		self.rightButtonPrompts:addElement( missilePrompt )
	end
end

