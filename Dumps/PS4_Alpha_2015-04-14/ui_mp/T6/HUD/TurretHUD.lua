CoD.TurretHUD = {}
LUI.createMenu.turret = function ( controller )
	local self = CoD.AirVehicleHUD.new( "turret", controller )
	self:addCompass()
	local inner = LUI.UIImage.new()
	inner:setLeftRight( false, false, -256, 256 )
	inner:setTopBottom( false, false, -256, 256 )
	inner:setImage( RegisterMaterial( "mp_hud_sentry_inner" ) )
	self:addElement( inner )
	local outerMaterial = RegisterMaterial( "mp_hud_sentry_outer_white" )
	local outerWidth = 512
	local outerHeight = 256
	local outerRightOffset = 80
	local outerBottomOffset = 20
	local outerBottomRight = LUI.UIImage.new()
	outerBottomRight:setLeftRight( false, false, outerRightOffset, outerRightOffset + outerWidth )
	outerBottomRight:setTopBottom( false, false, outerBottomOffset, outerBottomOffset + outerHeight )
	outerBottomRight:setImage( outerMaterial )
	outerBottomRight:setRGB( self.colorR, self.colorG, self.colorB )
	self:addElement( outerBottomRight )
	local outerBottomLeft = LUI.UIImage.new()
	outerBottomLeft:setLeftRight( false, false, -outerRightOffset, -outerRightOffset - outerWidth )
	outerBottomLeft:setTopBottom( false, false, outerBottomOffset, outerBottomOffset + outerHeight )
	outerBottomLeft:setImage( outerMaterial )
	outerBottomLeft:setRGB( self.colorR, self.colorG, self.colorB )
	self:addElement( outerBottomLeft )
	local outerTopRight = LUI.UIImage.new()
	outerTopRight:setLeftRight( false, false, outerRightOffset, outerRightOffset + outerWidth )
	outerTopRight:setTopBottom( false, false, -outerBottomOffset, -outerBottomOffset - outerHeight )
	outerTopRight:setImage( outerMaterial )
	outerTopRight:setRGB( self.colorR, self.colorG, self.colorB )
	self:addElement( outerTopRight )
	local outerTopLeft = LUI.UIImage.new()
	outerTopLeft:setLeftRight( false, false, -outerRightOffset, -outerRightOffset - outerWidth )
	outerTopLeft:setTopBottom( false, false, -outerBottomOffset, -outerBottomOffset - outerHeight )
	outerTopLeft:setImage( outerMaterial )
	outerTopLeft:setRGB( self.colorR, self.colorG, self.colorB )
	self:addElement( outerTopLeft )
	if CoD.IS_KILLCAM_OR_SPECTATE( controller ) == 0 and not Engine.IsDemoPlaying() then
		local exitPrompt = LUI.UIText.new()
		exitPrompt:setLeftRight( true, false, 0, 0 )
		exitPrompt:setTopBottom( false, true, -30 - CoD.textSize.Default, -30 )
		exitPrompt:setText( Engine.Localize( "MP_REMOTE_EXIT" ) )
		exitPrompt:setFont( CoD.fonts.Default )
		exitPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		self.leftButtonPrompts:addElement( exitPrompt )
		local zoomPrompt = LUI.UIText.new()
		zoomPrompt:setLeftRight( true, false, 0, 0 )
		zoomPrompt:setTopBottom( false, true, -CoD.textSize.Default, 0 )
		zoomPrompt:setText( Engine.Localize( "KILLSTREAK_INCREASE_ZOOM" ) )
		zoomPrompt:setFont( CoD.fonts.Default )
		zoomPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		self.leftButtonPrompts:addElement( zoomPrompt )
		local gunPrompt = LUI.UIText.new()
		gunPrompt:setLeftRight( false, true, -1000, 0 )
		gunPrompt:setTopBottom( false, true, -CoD.textSize.Default, 0 )
		gunPrompt:setText( Engine.Localize( "MP_FIRE_SENTRY_GUN" ) )
		gunPrompt:setFont( CoD.fonts.Default )
		gunPrompt:setAlignment( LUI.Alignment.Right )
		gunPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		self.rightButtonPrompts:addElement( gunPrompt )
	end
	return self
end

