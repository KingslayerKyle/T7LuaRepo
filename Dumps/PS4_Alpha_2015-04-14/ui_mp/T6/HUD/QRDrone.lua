CoD.QRDrone = {}
LUI.createMenu.qrdrone_mp = function ( controller )
	local self = CoD.AirVehicleHUD.new( "qrdrone_mp", controller, nil, nil, nil, "digital_noise_filter_qrdrone" )
	self:addCompass()
	local bracketMaterial = RegisterMaterial( "mp_hud_scorestreak_bracket" )
	local bracketWidth = 64
	local bracketHeight = 512
	local bracketOffset = 400
	local leftBracket = LUI.UIImage.new()
	leftBracket:setLeftRight( false, false, -bracketOffset, -bracketOffset + bracketWidth )
	leftBracket:setTopBottom( false, false, -bracketHeight / 2, bracketHeight / 2 )
	leftBracket:setImage( bracketMaterial )
	leftBracket:setRGB( self.colorR, self.colorG, self.colorB )
	self:addElement( leftBracket )
	local rightBracket = LUI.UIImage.new()
	rightBracket:setLeftRight( false, false, bracketOffset, bracketOffset - bracketWidth )
	rightBracket:setTopBottom( false, false, -bracketHeight / 2, bracketHeight / 2 )
	rightBracket:setImage( bracketMaterial )
	rightBracket:setRGB( self.colorR, self.colorG, self.colorB )
	self:addElement( rightBracket )
	local inner = LUI.UIImage.new()
	inner:setupRollLevel( 1 )
	inner:setLeftRight( false, false, -256, 256 )
	inner:setTopBottom( false, false, -128, 128 )
	inner:setImage( RegisterMaterial( "mp_hud_wasp_inner_white" ) )
	inner:setRGB( self.colorR, self.colorG, self.colorB )
	self:addElement( inner )
	local outer = LUI.UIImage.new()
	outer:setupRollLevel( -1 )
	outer:setLeftRight( false, false, -256, 256 )
	outer:setTopBottom( false, false, -256, 256 )
	outer:setImage( RegisterMaterial( "mp_hud_wasp_outer_white" ) )
	outer:setRGB( self.colorR, self.colorG, self.colorB )
	self:addElement( outer )
	local crosshair = LUI.UIImage.new()
	crosshair:setLeftRight( false, false, -32, 32 )
	crosshair:setTopBottom( false, false, -32, 32 )
	crosshair:setImage( RegisterMaterial( "mp_hud_wasp_crosshair_white" ) )
	crosshair:setRGB( self.colorR, self.colorG, self.colorB )
	self:addElement( crosshair )
	local recordingBoxScaleContainer = CoD.SplitscreenScaler.new( nil, CoD.SplitscreenMultiplier )
	recordingBoxScaleContainer:setLeftRight( false, true, 0, 0 )
	recordingBoxScaleContainer:setTopBottom( false, true, 0, 0 )
	self.safeAreaContainer:addElement( recordingBoxScaleContainer )
	local recordingTextBox = LUI.UIElement.new()
	recordingTextBox:setLeftRight( false, true, -100, 0 )
	recordingTextBox:setTopBottom( false, true, -CoD.textSize.Condensed - 4 - CoD.AirVehicleHUD.BottomBarHeight, -CoD.AirVehicleHUD.BottomBarHeight )
	recordingBoxScaleContainer:addElement( recordingTextBox )
	recordingTextBox:addElement( CoD.BorderT6.new( 2, CoD.AirVehicleHUD.HUDColor.r, CoD.AirVehicleHUD.HUDColor.g, CoD.AirVehicleHUD.HUDColor.b, CoD.AirVehicleHUD.Alpha ) )
	local recordingHeader = LUI.UIText.new()
	recordingHeader:setLeftRight( true, false, -40, 0 )
	recordingHeader:setTopBottom( false, false, -CoD.textSize.Condensed / 2, CoD.textSize.Condensed / 2 )
	recordingHeader:setRGB( CoD.AirVehicleHUD.HUDColor.r, CoD.AirVehicleHUD.HUDColor.g, CoD.AirVehicleHUD.HUDColor.b )
	recordingHeader:setAlpha( CoD.AirVehicleHUD.Alpha )
	recordingHeader:setFont( CoD.fonts.Condensed )
	recordingHeader:setText( "REC" )
	recordingTextBox:addElement( recordingHeader )
	local recordingText = LUI.UIText.new()
	recordingText:setLeftRight( true, false, 13, 14 )
	recordingText:setTopBottom( false, false, -CoD.textSize.Condensed / 2, CoD.textSize.Condensed / 2 )
	recordingText:setRGB( CoD.AirVehicleHUD.HUDColor.r, CoD.AirVehicleHUD.HUDColor.g, CoD.AirVehicleHUD.HUDColor.b )
	recordingText:setFont( CoD.fonts.Condensed )
	recordingText:setAlpha( CoD.AirVehicleHUD.Alpha )
	recordingText:setUseGameTime( true )
	recordingText:setText( "00:00.00" )
	recordingText.milliseconds = 0
	recordingText.seconds = 0
	recordingText:registerEventHandler( "countup_tick", CoD.AirVehicleHUD.RecordingTimerTick )
	recordingTextBox:addElement( recordingText )
	recordingTextBox:addElement( LUI.UITimer.new( 10, "countup_tick", false, recordingText ) )
	if CoD.IS_KILLCAM_OR_SPECTATE( controller ) == 0 and not Engine.IsDemoPlaying() then
		local descendPrompt = LUI.UIText.new()
		descendPrompt:setLeftRight( true, false, 0, 0 )
		descendPrompt:setTopBottom( false, true, -CoD.textSize.Default, 0 )
		descendPrompt:setText( Engine.Localize( "MP_QRDRONE_DESCEND" ) )
		descendPrompt:setFont( CoD.fonts.Default )
		descendPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		self.leftButtonPrompts:addElement( descendPrompt )
		local exitPrompt = LUI.UIText.new()
		exitPrompt:setLeftRight( true, false, 0, 0 )
		exitPrompt:setTopBottom( false, true, -30 - CoD.textSize.Default, -30 )
		exitPrompt:setText( Engine.Localize( "MP_REMOTE_EXIT" ) )
		exitPrompt:setFont( CoD.fonts.Default )
		exitPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		self.leftButtonPrompts:addElement( exitPrompt )
		local ascendPrompt = LUI.UIText.new()
		ascendPrompt:setLeftRight( false, true, -1000, 0 )
		ascendPrompt:setTopBottom( false, true, -CoD.textSize.Default, 0 )
		ascendPrompt:setText( Engine.Localize( "MP_QRDRONE_ASCEND" ) )
		ascendPrompt:setFont( CoD.fonts.Default )
		ascendPrompt:setAlignment( LUI.Alignment.Right )
		ascendPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		self.rightButtonPrompts:addElement( ascendPrompt )
		local attackPrompt = LUI.UIText.new()
		attackPrompt:setLeftRight( false, true, -1000, 0 )
		attackPrompt:setTopBottom( false, true, -30 - CoD.textSize.Default, -30 )
		attackPrompt:setText( Engine.Localize( "MP_QRDRONE_ATTACK" ) )
		attackPrompt:setFont( CoD.fonts.Default )
		attackPrompt:setAlignment( LUI.Alignment.Right )
		attackPrompt:setRGB( self.colorR, self.colorG, self.colorB )
		self.rightButtonPrompts:addElement( attackPrompt )
	end
	return self
end

