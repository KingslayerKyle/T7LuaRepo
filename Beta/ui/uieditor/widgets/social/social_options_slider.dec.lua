			SetProperty( self, "hideArrows", true )
			UpdateElementState( self, "Slider", controller )
		elseif IsElementInState( element, "Selected" ) then
			SetProperty( self, "hideArrows", false )
			UpdateElementState( self, "Slider", controller )
		end
	end )
	Slider.id = "Slider"
	self:registerEventHandler( "gain_focus", function ( self, event )
		if self.m_focusable and self.Slider:processEvent( event ) then
			return true
		else
			return LUI.UIElement.gainFocus( self, event )
		end
	end )
	LUI.OverrideFunction_CallOriginalSecond( self, "close", function ( self )
		self.Socialbackground000:close()
		self.Socialbackground00:close()
		self.Slider:close()
		self.FocusBarT:close()
		self.FocusBarB:close()
		self.FocusBorder:close()
		self.Title:close()
		self.Icon:close()
	end )
	if PostLoadFunc then
		PostLoadFunc( self, controller, menu )
	end
	return self
end

